// Reads and migrates files to the specified Relational MySQL structure
function readDb(): DatabaseSchema {
  try {
    if (!fs.existsSync(DB_PATH)) {
      const initialDb = writeInitialSeed();
      return initialDb;
    }
    const raw = fs.readFileSync(DB_PATH, "utf-8");
    const data = JSON.parse(raw);
    
    // Ensure separate arrays (relational simulation) exist on load
    let migrated = false;
    
    if (!data.users) { data.users = []; migrated = true; }
    if (!data.domains) { data.domains = []; migrated = true; }
    if (!data.courses) { data.courses = []; migrated = true; }
    if (!data.lessons) { data.lessons = []; migrated = true; }
    if (!data.enrollments) { data.enrollments = []; migrated = true; }
    if (!data.progress) { data.progress = []; migrated = true; }
    if (!data.bookmarks) { data.bookmarks = []; migrated = true; }
    if (!data.comments) { data.comments = []; migrated = true; }
    if (!data.quizzes) { data.quizzes = []; migrated = true; }
    if (!data.quiz_history) { data.quiz_history = []; migrated = true; }
    if (!data.activityLogs) { data.activityLogs = []; migrated = true; }

    // Migrate any legacy plain-passwords to bcrypt
    data.users.forEach((u: any) => {
      if (u.password && !u.password.startsWith("$2a$") && !u.password.startsWith("$2b$")) {
        u.password = bcrypt.hashSync(u.password, 10);
        migrated = true;
      }
      if (!u.skills) { u.skills = ["Node.js", "React"]; migrated = true; }
      if (!u.learning_interests) { u.learning_interests = ["Web Development", "AI & Machine Learning"]; migrated = true; }
    });

    // Extract dynamic Modules -> flat relational Lessons table on startup if empty
    if (data.lessons.length === 0 && data.courses && data.courses.length > 0) {
      data.courses.forEach((course: any) => {
        if (course.modules) {
          course.modules.forEach((mod: any) => {
            if (mod.lessons) {
              mod.lessons.forEach((les: any) => {
                data.lessons.push({
                  id: les.id,
                  course_id: course.id,
                  title: les.title,
                  content: generateMarkdownContentForLesson(les.id, les.title),
                  module_id: mod.id,
                  module_title: mod.title,
                  duration: les.duration || "15:00",
                  isLocked: les.isLocked ?? true,
                  preview: les.preview ?? false
                });
              });
            }
          });
          // Remove modules from core object so courses properties matches MySQL strictly
          delete course.modules;
          migrated = true;
        }
      });
    }

    // Map Domain relational keys if old attributes
    data.domains.forEach((dom: any) => {
      if (dom.name && !dom.domain_name) {
        dom.domain_name = dom.name;
        migrated = true;
      }
    });

    if (migrated) {
      fs.writeFileSync(DB_PATH, JSON.stringify(data, null, 2), "utf-8");
    }

    return data;
  } catch (error) {
    console.error("Database reading error, generating clean seed", error);
    return writeInitialSeed();
  }
}

