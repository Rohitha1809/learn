app.post("/api/quizzes/:id/submit", authenticateJWT, (req: AuthenticatedRequest, res) => {
  const { answers } = req.body; // user answers keyed like {q1: "Option text" or "True"...}
  if (!answers) return res.status(400).json({ error: "Answer payload requested" });

  const db = readDb();
  const quiz = db.quizzes.find(q => q.id === req.params.id);
  if (!quiz) return res.status(404).json({ error: "Quiz evaluation not found" });

  let score = 0;
  const evaluationResults: any[] = [];

  quiz.questions.forEach((qObj: any) => {
    const userAns = answers[qObj.id];
    let isCorrect = false;

    if (qObj.type === "mcq") {
      const correctIdx = Number(qObj.correct_option);
      const correctAnswerText = qObj.options[correctIdx];
      isCorrect = String(userAns).trim().toLowerCase() === String(correctAnswerText).trim().toLowerCase();
      evaluationResults.push({
        id: qObj.id,
        question: qObj.question,
        type: "mcq",
        userAnswer: userAns || "Unanswered",
        correctAnswer: correctAnswerText,
        isCorrect
      });
    } else if (qObj.type === "true_false") {
      isCorrect = String(userAns).trim().toLowerCase() === String(qObj.correct_option).trim().toLowerCase();
      evaluationResults.push({
        id: qObj.id,
        question: qObj.question,
        type: "true_false",
        userAnswer: userAns || "Unanswered",
        correctAnswer: qObj.correct_option,
        isCorrect
      });
    } else if (qObj.type === "short") {
      const expectedKeyword = String(qObj.correct_answer).trim().toLowerCase();
      isCorrect = String(userAns).trim().toLowerCase().includes(expectedKeyword);
      evaluationResults.push({
        id: qObj.id,
        question: qObj.question,
        type: "short",
        userAnswer: userAns || "Unanswered",
        correctAnswer: `Should include: "${qObj.correct_answer}"`,
        isCorrect
      });
    }

    if (isCorrect) score += 1;
  });

  const percentScore = Math.round((score / quiz.questions.length) * 100);

  // Award XP inside database Users table
  const user = db.users.find(u => u.id === req.user?.id);
  const xpAward = score * 10;
  if (user) {
    user.xp = (user.xp || 100) + xpAward;
  }

  const record: DBQuizHistory = {
    id: "qh_" + Math.random().toString(36).substring(2),
    user_id: req.user?.id || "u1",
    quiz_id: quiz.id,
    score,
    total_questions: quiz.questions.length,
    answers,
    timestamp: new Date().toISOString()
  };

  db.quiz_history.push(record);
  writeDb(db);

  res.json({
    success: true,
    score,
    totalQuestions: quiz.questions.length,
    percentScore,
    xpAwarded: xpAward,
    results: evaluationResults
  });
});

